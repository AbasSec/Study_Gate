// Current language (default: English)
let currentLanguage = localStorage.getItem('horizons-language') || 'en';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    initializeLanguageSystem();
});

// Initialize language system
function initializeLanguageSystem() {
    applyLanguageSettings();
    setupLanguageToggle();
}

// Get current language
function getCurrentLanguage() {
    return currentLanguage;
}

// Set language
function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('horizons-language', lang);
        applyLanguageSettings();

        // Dispatch event for language change
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
    }
}

// Get translated string
function t(key) {
    const keys = key.split('.');
    let value = translations[currentLanguage];

    for (let k of keys) {
        if (value && typeof value === 'object') {
            value = value[k];
        } else {
            return key; // Return key if translation not found
        }
    }

    return value || key;
}

// Apply language settings (direction, translations, etc.)
function applyLanguageSettings() {
    // Set HTML direction
    const isArabic = currentLanguage === 'ar';
    document.documentElement.dir = isArabic ? 'rtl' : 'ltr';
    document.documentElement.lang = currentLanguage;

    // Add/remove RTL class
    document.documentElement.classList.toggle('rtl', isArabic);

    // Update all translated elements
    updatePageTranslations();

    // Update language toggle button
    updateLanguageToggle();
}

// Update all data-translate elements on the page
function updatePageTranslations() {
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        const translated = t(key);

        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = translated;
        } else if (el.querySelector('span,svg,i,img')) {
            // Element has child elements (icon etc.) — update only the first text node
            let updated = false;
            for (const node of el.childNodes) {
                if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
                    const trailingSpace = node.textContent.match(/\s+$/) ? ' ' : '';
                    node.textContent = translated + trailingSpace;
                    updated = true;
                    break;
                }
            }
            // If no leading text node found, prepend one (icon-only then text was added)
            if (!updated) el.insertBefore(document.createTextNode(translated + ' '), el.firstChild);
        } else {
            el.textContent = translated;
        }
    });

    // Update data-translate-placeholder for input placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        el.placeholder = t(key);
    });

    // Update data-translate-title for titles/alt text
    document.querySelectorAll('[data-translate-title]').forEach(el => {
        const key = el.getAttribute('data-translate-title');
        el.title = t(key);
        el.alt = t(key);
    });
}

// Setup language toggle (guard prevents double-binding when called multiple times)
function setupLanguageToggle() {
    const toggleBtn = document.getElementById('languageToggle');
    if (!toggleBtn) return;
    if (!toggleBtn.dataset.langWired) {
        toggleBtn.addEventListener('click', toggleLanguage);
        toggleBtn.dataset.langWired = '1';
    }
    updateLanguageToggle();
}

// Toggle language
function toggleLanguage() {
    const newLang = currentLanguage === 'en' ? 'ar' : 'en';
    setLanguage(newLang);
}

// Update language toggle button display
function updateLanguageToggle() {
    const toggleBtn = document.getElementById('languageToggle');
    if (!toggleBtn) return;

    const label = currentLanguage === 'en' ? 'العربية' : 'English';
    toggleBtn.setAttribute('aria-label', `Switch to ${currentLanguage === 'en' ? 'Arabic' : 'English'}`);

    // If the button contains a .lang-label span, update only that span
    const labelSpan = toggleBtn.querySelector('.lang-label');
    if (labelSpan) {
        labelSpan.textContent = label;
    } else {
        // Fallback for buttons that contain only text (older pages)
        toggleBtn.textContent = label;
    }
}

